numeroUm = int(input('Digite o primeiro número:'))
numeroDois = int(input('Digite o segundo número:'))
numeroTres = int(input('Digite o terceiro número:'))
listaNumeros = [numeroUm, numeroDois, numeroTres]

print('O maior número é: {}'.format(max(listaNumeros)))
print('O menor número é: {}'.format(min(listaNumeros)))
